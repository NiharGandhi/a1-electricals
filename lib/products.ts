/** Column definition for catalog specification tables */
export interface SpecTableColumn {
  key: string;
  label: string;
  unit?: string;
}

/** Tabular specification data from product catalog/datasheet */
export interface SpecificationTable {
  columns: SpecTableColumn[];
  rows: Record<string, string | number>[];
  /** Optional footnotes shown below table (e.g. code/value mapping) */
  notes?: string[];
}

export interface Product {
  slug: string;
  title: string;
  category: string;
  categorySlug: string;
  description: string;
  image: string;
  features: string[];
  specs?: string[];
  standards: string[];
  applications: string[];
  /** Show variant drawing image beside specification table (for technical drawings only) */
  showDrawingWithSpecification?: boolean;
  /** Optional catalog-style specification table (dimensions, part numbers, etc.) */
  specificationTable?: SpecificationTable;
  /** Optional multiple catalog-style specification tables for product variants/types */
  specificationTables?: {
    title: string;
    tabLabel?: string;
    /** Optional main product image for this variant/tab */
    image?: string;
    /** Optional technical drawing image for this variant */
    drawingImage?: string;
    /** Optional multiple technical drawing images for this variant */
    drawingImages?: string[];
    table: SpecificationTable;
  }[];
  /** Optional multiple technical drawing images for single-table products */
  specificationDrawingImages?: string[];
}

